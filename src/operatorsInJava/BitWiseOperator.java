package operatorsInJava;

public class BitWiseOperator {
public static void main(String args[]) {
	int a=20,b=16;
	System.out.println("a&b = "+(a&b));
	System.out.println("a|b = "+(a|b));
	System.out.println("a^b = "+(a^b));
	System.out.println("a>>2 = "+(a>>2));
	System.out.println("a>>3 = "+(a>>3));
	System.out.println("a<<2 = "+(a<<2));
	System.out.println("~(5) = "+(~(5)));
	System.out.println("~(-5) = "+(~(-5)));
}
}
