package operatorsInJava;
import java.util.*;
public class DivisonAndModulusOper {
public static void main(String args[]) {
	Scanner sc=new Scanner(System.in);
	System.out.println("Input the two values : ");
	byte a = sc.nextByte();
	byte b = sc.nextByte();
	byte c = (byte)(a+b);//type casting converting one data type into another
	System.out.println("Addition of two byte is "+c);
}
}
